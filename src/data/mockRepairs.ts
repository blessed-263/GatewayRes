import { format } from "date-fns";
import type { PartRequest, Repair, RepairAttachment } from "@/types/repair";

const today = format(new Date(), "yyyy-MM-dd");
const tomorrow = format(new Date(Date.now() + 86400000), "yyyy-MM-dd");

function seedAttachment(
  repairId: string,
  kind: RepairAttachment["kind"],
  name: string,
  url: string,
  uploadedAt: string,
  uploadedBy: string
): RepairAttachment {
  return {
    id: `att-${repairId}-${kind}`,
    repairId,
    url,
    filename: name,
    originalName: name,
    mimeType: "image/jpeg",
    size: 240000,
    kind,
    uploadedAt,
    uploadedBy,
  };
}

function seedPart(
  repairId: string,
  partName: string,
  status: PartRequest["status"],
  requestedBy: string,
  requestedAt: string,
  quantity = 1
): PartRequest {
  return {
    id: `part-${repairId}-${partName.replace(/\s+/g, "-").toLowerCase()}`,
    repairId,
    requestedKind: "part",
    partName,
    quantity,
    status,
    requestedBy,
    requestedAt,
    updatedAt: requestedAt,
    description: undefined,
    supplier: status === "ordered" ? "BuildIt JHB" : undefined,
    estimatedCost: 180,
  };
}

